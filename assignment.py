'''
=	x = 4	Assign 4 to x
+=	x += 4	Add 4 to existing value of x
-=	x -= 4	Subtract 4 from existing value of x
*=	x *= 4	Multiply existing value by 4
/=	x /= 4	Divide existing value by 4
%=	x %= 4	Modulo existing value by 4
'''
x = 4

x += 4  # x is 8
x -= 4  # x is 0
x *= 4  # x is 16
x /= 4  # x is 1.0
x %= 4  # x is 0

